//=========================================================================
- printWithLength: (int) iV;
{
   int  i;
   int* bodyPtr=body;

   printf
   ("\n%8X length: %4d \n",
    (int) self, iV);

   printf("\n%4d ",*bodyPtr++);
   for(i=1;i<iV;i++)
   {
      if(i%16 == 0) printf("\n");
      printf("%4d ",*bodyPtr++);
   }
   printf("\n");
   return self;
}
